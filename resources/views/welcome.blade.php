    <script>
        const canvas = document.getElementById('wheel');
        const ctx = canvas.getContext('2d');
        let rewards = @json($rewards); // Dữ liệu phần thưởng từ controller
        let isSpinning = false;
        let currentAngle = 0;

        // Đảm bảo canvas hiển thị tròn
        const container = document.querySelector('.wheel-container');
        const size = Math.min(container.clientWidth, container.clientHeight);
        canvas.width = size * 2; // Tăng độ phân giải để mượt hơn
        canvas.height = size * 2;
        canvas.style.width = `${size}px`;
        canvas.style.height = `${size}px`;
        ctx.scale(2, 2); // Điều chỉnh tỷ lệ để vẽ trên canvas độ phân giải cao

        // Vẽ vòng quay
        function drawWheel() {
            const centerX = size / 2;
            const centerY = size / 2;
            const radius = (size / 2) - 10;
            const numSegments = rewards.length;
            const anglePerSegment = (2 * Math.PI) / numSegments;

            ctx.clearRect(0, 0, canvas.width, canvas.height);

            // Vẽ các phần của vòng quay
            for (let i = 0; i < numSegments; i++) {
                const startAngle = i * anglePerSegment + currentAngle;
                const endAngle = (i + 1) * anglePerSegment + currentAngle;

                ctx.beginPath();
                ctx.moveTo(centerX, centerY);
                ctx.arc(centerX, centerY, radius, startAngle, endAngle);
                ctx.fillStyle = rewards[i].color;
                ctx.fill();
                ctx.strokeStyle = '#fff';
                ctx.lineWidth = 2;
                ctx.stroke();

                // Vẽ tên phần thưởng
                ctx.save();
                ctx.translate(centerX, centerY);
                ctx.rotate(startAngle + anglePerSegment / 2);
                ctx.textAlign = 'center';
                ctx.fillStyle = '#fff';
                ctx.font = '16px Arial';
                ctx.fillText(rewards[i].name, radius / 2, 0);
                ctx.restore();
            }

            // Vẽ viền ngoài
            const borderWidth = size * 0.02; // Độ dày viền responsive
            ctx.beginPath();
            ctx.arc(centerX, centerY, radius + borderWidth / 2, 0, 2 * Math.PI);
            ctx.strokeStyle = '#ff0000';
            ctx.lineWidth = borderWidth;
            ctx.stroke();

            // Vẽ các chấm tròn trên viền
            for (let i = 0; i < 12; i++) {
                const angle = (i * 2 * Math.PI) / 12;
                const dotX = centerX + (radius + borderWidth / 2) * Math.cos(angle);
                const dotY = centerY + (radius + borderWidth / 2) * Math.sin(angle);
                ctx.beginPath();
                ctx.arc(dotX, dotY, 5, 0, 2 * Math.PI);
                ctx.fillStyle = '#ffd700';
                ctx.fill();
            }

            // Vẽ nút "Start" ở giữa
            drawStartButton(centerX, centerY);
        }

        // Hàm vẽ nút "Start"
        function drawStartButton(centerX, centerY) {
            const buttonRadius = 50; // Bán kính nút

            // Vẽ nút tròn màu vàng
            ctx.beginPath();
            ctx.arc(centerX, centerY, buttonRadius, 0, 2 * Math.PI);
            ctx.fillStyle = '#ffd700'; // Màu vàng
            ctx.fill();
            ctx.strokeStyle = '#d4af37'; // Viền vàng đậm hơn
            ctx.lineWidth = 5;
            ctx.stroke();

            // Vẽ chữ "START"
            ctx.font = 'bold 20px Arial';
            ctx.fillStyle = '#ff0000'; // Chữ màu đỏ
            ctx.textAlign = 'center';
            ctx.textBaseline = 'middle';
            ctx.fillText('START', centerX, centerY);

            // Vẽ mũi tên nhỏ bên phải
            const arrowSize = 15;
            const arrowX = centerX + buttonRadius - 5;
            const arrowY = centerY;
            ctx.beginPath();
            ctx.moveTo(arrowX, arrowY - arrowSize / 2);
            ctx.lineTo(arrowX + arrowSize, arrowY);
            ctx.lineTo(arrowX, arrowY + arrowSize / 2);
            ctx.closePath();
            ctx.fillStyle = '#ff0000'; // Mũi tên màu đỏ
            ctx.fill();
            ctx.strokeStyle = '#d4af37'; // Viền mũi tên
            ctx.lineWidth = 2;
            ctx.stroke();
        }

        // Xử lý quay
        canvas.addEventListener('click', (event) => {
            if (isSpinning) return;

            // Kiểm tra xem click có nằm trong nút "Start" không
            const rect = canvas.getBoundingClientRect();
            const scaleX = canvas.width / rect.width;
            const scaleY = canvas.height / rect.height;
            const clickX = (event.clientX - rect.left) * scaleX / 2;
            const clickY = (event.clientY - rect.top) * scaleY / 2;
            const centerX = size / 2;
            const centerY = size / 2;
            const buttonRadius = 50;

            const distance = Math.sqrt((clickX - centerX) ** 2 + (clickY - centerY) ** 2);
            if (distance > buttonRadius) return; // Không click vào nút

            isSpinning = true;

            // Gọi API để lấy phần thưởng
            fetch('{{ route("wheel.spin") }}', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    'X-CSRF-TOKEN': '{{ csrf_token() }}'
                }
            })
            .then(response => response.json())
            .then(data => {
                const rewardName = data.reward;
                const rewardIndex = rewards.findIndex(r => r.name === rewardName);
                const numSegments = rewards.length;
                const anglePerSegment = (2 * Math.PI) / numSegments;

                // Tính góc dừng (giữa phần thưởng được chọn)
                const targetAngle = -(rewardIndex * anglePerSegment + anglePerSegment / 2) + 4 * 2 * Math.PI; // Quay thêm 4 vòng

                // Animation quay
                let startTime = null;
                const duration = 5000; // 5 giây

                function animate(time) {
                    if (!startTime) startTime = time;
                    const progress = (time - startTime) / duration;
                    const easeOut = 1 - Math.pow(1 - progress, 3); // Hiệu ứng chậm dần

                    currentAngle = easeOut * targetAngle;
                    drawWheel();

                    if (progress < 1) {
                        requestAnimationFrame(animate);
                    } else {
                        isSpinning = false;
                        alert(`Chúc mừng! Bạn đã nhận được: ${rewardName}`);
                    }
                }

                requestAnimationFrame(animate);
            });
        });

        // Vẽ vòng quay ban đầu
        drawWheel();
    </script>
